# Declaração das variáveis de número a partir do input do usuário
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# Verificação da condição pedida
if (num1 == num2) :
    print("Ambos os números são iguais")
else :
    print("Ambos os números são diferentes")