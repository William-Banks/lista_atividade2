# Declaração das três variáveis de número a partir do input do usuário
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
num3 = int(input("Digite o terceiro número: "))

# Verificação da condição pedida
if (num1 > num2, num1 < num3) :
    print("O primeiro número é maior que o segundo e menor que o terceiro simultaneamente.")
else :
    print("O primeiro número não é maior que o segundo e menor que o terceiro simultaneamente.")