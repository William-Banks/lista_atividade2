# Declaração das duas variáveis de número a partir do input do usuário
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# Verificação da condição pedida
if (num2 > num1) :
    print(f"{num2} é maior que {num1}")    
else : 
    print(f"{num2} não é maior que {num1}")